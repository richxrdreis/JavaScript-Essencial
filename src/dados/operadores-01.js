// Operadores aritméticos

// Usando valroes literais

console.log(10 + 5);
console.log(10 - 5);
console.log(10 * 5);
console.log(10 / 5);
console.log(10 % 5); // aqui é o resto da divisão

// Usando variáveis

let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// Incremento e decremento
let z = 10;

console.log(z++); // z = z + 1
console.log(z);
console.log(++z); // com o ++ na frente, quer dizer que tem uma prioridade maior. (se executar somente esta parte do código, apareceria 11 ao invés de 10 no console.)

console.log(z--);
console.log(z);
console.log(--z);
