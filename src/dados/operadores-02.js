// Operadores relacionais

// Usando valores literais

console.log(10 > 5); // true
console.log(10 < 5); // false
console.log(10 >= 5); // true
console.log(10 <= 5); // false
console.log(10 == 5); // false
console.log(10 != 5); // true
console.log(10 == '10'); // true
console.log(10 === '10'); // false

// (=) atribuição
//  (==) comparação
//  (===) comparação estrita (pra saber o tipo, seja ele string, number...)
// Obs importante: também temos a diferença estrita (!==) ! ==

// Usando variáveis
let x = 10;
let y = 5;

console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x == y);
console.log(x != y);
console.log(x == '10');
console.log(x === '10');
